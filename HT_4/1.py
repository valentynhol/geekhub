"""
1. Написати функцію < square > , яка прийматиме один аргумент - 
сторону квадрата, і вертатиме 3 значення (кортеж):
периметр квадрата, площа квадрата та його діагональ.
"""

import math

def square(a, *args):
    P = 4 * a
    S = a ** 2
    d = math.sqrt((a**2)*2)
    return P, S, d

a = int(input('Введіть розмір сторони квадрата: '))
print(square(a))
